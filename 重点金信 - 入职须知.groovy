2. 功能介绍
    1) 人才档案及更新 -> 个人邮箱、手机号、住址更新等

    2) 我的薪资 -> 次月5号发工资 -> 考勤扣款, 社保扣款, 工资明细

    3) 考勤与休假
        带薪休假(小时) -> 年假周期(本年度07.01 ~ 次年度06.30) -> 一天8小时, 5天共40小时

        请假类型 -> 有薪休假 -> 年假
                -> 事假 -> 不带薪的请假
                -> 病假 -> 按照北京市最低薪资的80%进行折扣发放 -> 这个是要上传相关医院的证明附件
                -> 婚假 -> 北京是10天, 这个10天是入职后领证一年内一次性修完, 这10天如果中间有节假日或者周末也计算在内, 当然需要提交结婚证
                -> 生育产假 -> 也是自然连续日 -> 开始日期最早可在预产期前15天, 最晚不能晚于子女出生日期 -> 产假天数158, 难产假天数15天 -> 需要提供预产期证明和准生证, 或者子女出生证明, 难产证明
                -> 哺乳假 -> 不可以累积 -> 每天一个小时 -> 可以晚来一个小时或者早走一个小时 -> 条件是在孩子的一周岁以内
                -> 育儿假(男性女性都有, 给0 ~ 3周岁的宝宝的父母) -> 5天带薪年假 -> 需要出生证明验证3周岁以内, 上家单位未修育儿假的证明
                -> 护理假(给宝爸, 就是陪产假) -> 北京是15个自然日, 子女出生一个月内一次性休完 -> 需要配偶的预产期证明和孩子的准生证或者出生证明, 可以提前请, 在月末把请假证明拿出来就行
                -> 流产假 -> 北京: 妊娠16周及以下, 15天; 大于16周小于等于28周, 42天; 28周以上98天; -> 也是自然日, 需要流产证明和结婚证
                -> 产前检查 -> 16次可用机会, 每次时间是4小时 -> 需要提供医院的产检证明或者挂号证等相关的材料
                -> 丧假 -> 分为直系亲属(3天, 也是自然日)和旁系亲属(1天)
                -> 调休假 -> 针对在行里面周六周日的加班, 且被行里承认和审批 -> 那么就可以在"加班"这一项提交加班开始和结束日期, 并写加班理由, 同时拍照审批条, 后勤会进行备案 -> 审批通过以后就会在"请假"那里变为调休假, 同样是带新的, 下次请假的时候就可以去用, 每位同事每年可以申请的调休假有40小时, 如果说你这一年加班调休超了40, 假设48小时, 那么就会有8小时超额, 这8小时会被折现成8小时的加班费(初级每小时30元、中级每小时40元、高级每小时55元。)

    4) 报销
        报销单 -> 新建报销单 -> 加班晚, 回家报销车费( 报销加班车费: 加班21:00以后可以打车到地铁口, 采用出租车+公共交通工具结合。23:00以后可以打车回家(报销要附加行程单) ) -> 下一步, 费用信息, 手录费用 -> 交通费-市内网约车费 -> 上传发票的pdf, 附件上传行程单, 建议网约车滴滴, 它有行程单 -> 通过之后, 会发邮件通知通过, 接着就需要打印报销单 -> 这时候就需要告知助理, 让其代为打印提交

