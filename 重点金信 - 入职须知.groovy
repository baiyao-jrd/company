    7) 我的薪酬福利
        社保、公积金
        商保: 公司的第六险 -> 补充医疗保险 -> 二级及以上公立医院划医保卡进行急诊或者住院治疗, 100%赔付社保范围内个人自付的费用
                以某项费用为例, 假设该项目的费用为100元, 医保报销比例为80%。如果该项目属于甲类, 那么医保可以报销80元, 个人只需支付20元。若该项目属于乙类, 个人需要先支付10%, 即10元, 剩余的90元再按80%的比例报销, 即73元, 个人还需支付27元。若该项目属于丙类, 个人需要先支付20%, 即20元, 剩余的80元再按80%的比例报销, 即64元, 个人还需支付34元。

